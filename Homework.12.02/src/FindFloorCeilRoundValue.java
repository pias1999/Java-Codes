//double a=56.34, b=6.58334, c=-34.4265;
import java.lang.*;
	
public class FindFloorCeilRoundValue {

	public static void main(String[] args) {
		double b = 6.58334;
		double c = -34.4265;
		
		System.out.println("Floor value of B is: " + Math.floor(b));
		System.out.println("Ceil value of B is: " + Math.ceil(b));
		System.out.println("Round value of B is: " + Math.round(b));
		
		System.out.println("Floor value of C is: " + Math.floor(c));
		System.out.println("Ceil value of C is: " + Math.ceil(c));
		System.out.println("Round value of C is: " + Math.round(c));

	}

}
