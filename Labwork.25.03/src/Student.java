
public class Student {
	
	String ID;
	String Name;
	
	
	public Student(String iD, String name) {
		
		ID = iD;
		Name = name;
	}

	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

}
